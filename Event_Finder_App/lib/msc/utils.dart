  //Functions
  String toOrdinal(number) {
    if ((number >= 10) && (number <= 19)) {
      return number.toString() + 'th';
    } else if ((number % 10) == 1) {
      return number.toString() + 'st';
    } else if ((number % 10) == 2) {
      return number.toString() + 'nd';
    } else if ((number % 10) == 3) {
      return number.toString() + 'rd';
    } else {
      return number.toString() + 'th';
    }
  }

  String toMonthName(monthNum) {
    switch (monthNum) {
      case 1:
        return 'January';
      case 2:
        return 'February';
      case 3:
        return 'March';
